package org.springframework.samples.petclinic.web;

import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.view;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;

/** Test class for {@link WelcomeController} */
@ExtendWith(MockitoExtension.class)
class WelcomeControllerTests {

  @InjectMocks private WelcomeController welcomeController;

  private MockMvc mockMvc;

  @BeforeEach
  void setup() {
    mockMvc = MockMvcBuilders.standaloneSetup(welcomeController).build();
  }

  @Test
  void testWelcome() throws Exception {
    mockMvc.perform(get("/")).andExpect(status().isOk()).andExpect(view().name("welcome"));
  }
}


